import request from '../utils/request'

/**
 * 饼图数据接口
 * @param {Object} params 参照接口文档
 */
function pieChart (params) {
  return request.post('/doorKeeperManager/typeStatistics', params)
}

/**
 * 折线图数据接口
 * @param {Object} params 参照接口文档
 */
function lineChart (params) {
  return request.post('/doorKeeperManager/numberStatistics', params)
}

export default { pieChart, lineChart }
